class VisaoCirculo:

    def start(self):
        print("DESAFIO 1 - CIRCULO")

    def getRaio(self):
        raio = int(input("Digite o raio do circulo"))
        return raio

    def exibirPerimetro(self, perimetro):
        print("O perimetro do circulo é de %5.2f" % perimetro)

    def exibirArea(self, area):
        print("A area do circulo é de %5.2f " % area)


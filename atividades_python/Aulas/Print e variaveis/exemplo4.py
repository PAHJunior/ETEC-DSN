#Exemplo 4 conversão

num1 = int(input('digite um numero'))  # Recebe o valor digiado e converte em inteiro
num2 = int(input('digite outro numero'))   # Recebe o valor digiado e converte em inteiro
soma = num1 + num2  # realiza a soma entre num1 e num2
print('O resultado da soma é %i entre %i é = %i'% (num1, num2, soma))  # Exibe na tela o resultado de num1 e num2

num1 = float(input('Digite outro numero'))  # Recebe o valor digiado e converte em float
num2 = float(input('Digite outro numero'))  # Recebe o valor digiado e converte em float
soma = num1 + num2

print('O resulado da soma entre %i e %i é = %i'% (num1, num2, soma))  # Exibe na tela o resultado em float

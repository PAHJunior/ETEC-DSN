# Exemplo 5 - count

t = "um tigre, dois tigres, tres tigres" #Criando a string
print(t)
print("Na string tem ", t.count("tigre"), " tigre") #Conta quantas vezes encontrou tigre na string, nesse caso da 3
print("Na string tem ", t.count("tigres"), " tigres") #Conta quantas vezes encontrou tigres na string, nesse caso da 2
print("Na string tem ", t.count("t"), " t") #Conta quantas vezes encontrou a letra t na string, nesse caso da 4

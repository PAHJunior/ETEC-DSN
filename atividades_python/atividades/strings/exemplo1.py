# Exenoki 1 - Startswith e Endswith

nome = "João da Silva"
print(nome.startswith("João")) # Testa se a String começa com João, nesse caso retorna TRUE
print(nome.startswith("joão")) # Nesse caso retorna False por causa do j minusculo
print(nome.endswith("Silva")) # Testa se a string termina com Silva, retorna true
